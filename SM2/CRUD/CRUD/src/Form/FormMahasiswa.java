    private void b_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_addActionPerformed
    String nama = t_nama.getText();
    String telepon = t_telepon.getText();
    String alamat = t_alamat.getText();
    
    // Validasi input tidak boleh kosong
    if (nama.isEmpty() || telepon.isEmpty() || alamat.isEmpty()){
        JOptionPane.showMessageDialog(this, "Semua kolom harus diisi !", "Validasi", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        // Query INSERT tanpa menyertakan kolom 'id'
        String sql = "INSERT INTO tb_mahasiswa (nama, telepon, alamat) VALUES(?, ?, ?)";
        PreparedStatement st = conn.prepareStatement(sql);
        st.setString(1, nama);
        st.setString(2, telepon);
        st.setString(3, alamat);
        
        // Eksekusi query dan cek apakah data berhasil ditambahkan
        int rowInserted = st.executeUpdate();
        if (rowInserted > 0) {
            JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
            resetForm();
            getData();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        st.close();
    } catch (Exception e) {
        // Tampilkan pesan error
        Logger.getLogger(FormMahasiswa.class.getName()).log(Level.SEVERE, null, e);
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_b_addActionPerformed

    private void b_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_deleteActionPerformed
    int selectedRow = tbl_data.getSelectedRow();
    if(selectedRow == -1){
        JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus");
        return;
    }
    int confirm = JOptionPane.showConfirmDialog(this, "Apakah anda yakin ingin menghapus data ini?", "Konfirmasi",JOptionPane.YES_OPTION);
    if(confirm == JOptionPane.YES_OPTION){
        String id = tbl_data.getValueAt(selectedRow, 0).toString();
        
        try {
            String sql = "DELETE FROM tb_mahasiswa WHERE id=?";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, id);
                       
            int rowDelete = st.executeUpdate();
            if(rowDelete > 0){
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                resetForm();
                getData();
                
            }
            st.close();
        } catch (Exception e) {
            Logger.getLogger(FormMahasiswa.class.getName()).log(Level.SEVERE,null, e);
        }
        }
    }//GEN-LAST:event_b_deleteActionPerformed

